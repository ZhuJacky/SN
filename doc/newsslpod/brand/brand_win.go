var _testGenPinHook *bool

func init() {
	prometheus.MustRegister(promAutoUpdateWinRoot)
}

var promAutoUpdateWinRoot = prometheus.NewCounterVec(
	prometheus.CounterOpts{
		Name: "auto_update_win_root",
		Help: "自动更新windows根证书信任列表",
	},
	[]string{"status"},
)

var (
	winTrustRootURI = "https://raw.githubusercontent.com/robstradling/authroot.stl/master/authroot.tsv"

	mutex            sync.Mutex
	hashPinFromCache map[string]string // 预加载本地可信CA数据库中的hash与pin
)

func InitAutoUpdateWinRoot() { // init-->InitAutoUpdateWinRoot  不需要加载该包就启动该更新 只放到reportapp即可
	hashPinFromCache = map[string]string{}
	LoadRootsHashPin()
	// 异步更新 不阻塞主线程
	go AutoUpdateWinCert()
}

func LoadRootsHashPin() {
	for _, root := range GetCertStore().trustRoots {
		hashPinFromCache[root.Hash] = root.Pin
	}
}

func IsInWinTrustStore(root, midd *CertInfo) bool {
	mutex.Lock()
	defer mutex.Unlock()
	for _, hashPin := range winTrustCerts {
		if strings.ToLower(root.SHA1) == strings.ToLower(hashPin.Hash) {
			return true
		}
		if midd != nil {
			if strings.ToLower(midd.Pin) == strings.ToLower(hashPin.Pin) {
				return true
			}
		}
	}
	return false
}

func AutoUpdateWinCert() {
	log.Printf("auto update win trust trustRoots")
	if err := autoUpdateWinCert(context.Background()); err != nil {
		promAutoUpdateWinRoot.WithLabelValues("failed").Inc()
		log.Warnf("auto update win trust trustRoots err: %v", err)
	} else {
		promAutoUpdateWinRoot.WithLabelValues("success").Inc()
	}

	time.AfterFunc(time.Hour*24, AutoUpdateWinCert)
}

func autoUpdateWinCert(ctx context.Context) (err error) {
	resp, err := myconn.HttpRequest(ctx, "GET", winTrustRootURI, "", 1, nil, nil)
	if err != nil {
		return
	}
	defer resp.Body.Close()

	// hash
	hashPins := []HashPin{}
	bufReader := bufio.NewReader(resp.Body)
	for i := 0; ; i++ {
		line, isPrefix, err := bufReader.ReadLine()
		if err != nil {
			if err == io.EOF {
				break
			}
			return err
		}

		if isPrefix {
			continue
		}

		if i == 0 {
			continue
		}

		fields := strings.Split(string(line), "\t")
		if len(fields) != 11 {
			log.Warnf("win trust 未知格式字段:%+v", fields)
			continue
		}

		hash := fields[0]
		hashPins = append(hashPins, HashPin{hash, hashPinFromCache[hash]})
	}
	resp.Body.Close()

	if _testGenPinHook != nil && *_testGenPinHook == true {
		var str string
		for _, v := range hashPins {
			str = str + fmt.Sprintf("{\"%s\",\"%s\"},\n", v.Hash, v.Pin)
		}
		ioutil.WriteFile("pin.text", []byte(str), 0777)
	}

	certsCopy := make([]HashPin, len(winTrustCerts))
	mutex.Lock()
	copy(certsCopy, winTrustCerts)
	mutex.Unlock()

	// 新证书hash与原数据差异过大 提醒
	if len(hashPins) <= len(certsCopy)/2 || len(hashPins) > len(certsCopy)*3/2 {
		log.Warnf("win trust 数据差异过大，暂不更新: %v", hashPins)
		return
	}

	// 对比两个切片,比较新增的
	newCert := []HashPin{}
	for _, hashPin := range hashPins {
		exist := false
		for _, wHashPin := range certsCopy {
			if hashPin.Hash == wHashPin.Hash {
				exist = true
			}
		}
		if !exist {
			newCert = append(newCert, hashPin)
			log.Infof("win new cert: %v", hashPin.Hash)
			promAutoUpdateWinRoot.WithLabelValues("increase").Inc()
		}
	}

	// 对比两个切片, 找出是否有证书移除
	for _, wHashPin := range certsCopy {
		exist := false
		for _, hashPin := range hashPins {
			if wHashPin.Hash == hashPin.Hash {
				exist = true
			}
		}
		if !exist {
			log.Infof("win remove cert: %v", wHashPin.Hash)
			promAutoUpdateWinRoot.WithLabelValues("decrease").Inc()
		}
	}

	// 差别超过一半 提醒
	if len(newCert) >= len(certsCopy)/2 {
		log.Warnf("win trust 超过一半的证书更新: %v", newCert)
		return
	}

	mutex.Lock()
	winTrustCerts = hashPins
	mutex.Unlock()
	return nil
}

