func getBlackListPins() []string {

	list := make([]string, 0)

	for _, b := range rawblacklist {
		for _, cert := range b {
			certs, ok := certutils.GenCertsFromPem([]byte(cert))
			if !ok {
				pubdata, ok := certutils.GenPublicKeyFromPem([]byte(cert))
				if !ok {
					continue
				}
				pin := certutils.GenPin(pubdata)
				if !isExist(list, pin) {
					list = append(list, pin)
				}
			}
			if len(certs) == 0 {
				continue
			}
			pin := certutils.GenPin(certs[0].RawSubjectPublicKeyInfo)
			if !isExist(list, pin) {
				list = append(list, pin)
			}
		}
	}
	return list
}

func isExist(container []string, key string) bool {
	for _, content := range container {
		if content == key {
			return true
		}
	}
	return false
}
