// CipherID TODO
type CipherID uint32

// ToSSL2CipherRawBytes 生成ssl2的加密套件原始数据
func (c CipherID) ToSSL2CipherRawBytes() []byte {
	result := make([]byte, 3)
	result[0] = byte(c & 0xFF0000 >> 16)
	result[1] = byte(c & 0x00FF00 >> 8)
	result[2] = byte(c & 0x0000FF)

	return result
}

// ToTLSCipherRawBytes 生成tls的加密套件原始数据
func (c CipherID) ToTLSCipherRawBytes() []byte {
	result := make([]byte, 2)
	result[0] = byte(c & 0x00FF00 >> 8)
	result[1] = byte(c & 0x0000FF)
	return result
}

